function infoSpread () {
    let spread= 'O spread bancário é a diferença percentual entre a taxa de juros cobrada pelas instituições financeiras nos empréstimos e a taxa de juros paga nas atividades de captação dos bancos.' +
    '<br>Em termos simples, é a diferença entre os juros que o banco paga para captar recursos e os juros que ele cobra para emprestar dinheiro.' +
    '<br>' +
    '<br>O spread bancário é composto por diversos fatores, incluindo:' +
    '<br>' +
    '<br> - Custo de captação: os bancos precisam pagar juros para captar recursos dos clientes, como depósitos à vista, poupança e CDBs.' +
    '<br> - Risco de crédito: os bancos precisam avaliar o risco de crédito dos clientes para determinar a taxa de juros que cobrarão.' +
    '<br> - Custos operacionais: os bancos têm diversos custos operacionais, como custos de pessoal, infraestrutura e tecnologia.' +
    '<br> - Margem de lucro: os bancos precisam ter uma margem de lucro para remunerar seus acionistas e gestores.' +
    '<br>' +
    '<br>O spread bancário é um importante mecanismo para remunerar os bancos pelos serviços que eles prestam, como a concessão de empréstimos e financiamentos.' +
    '<br>No entanto, um spread bancário elevado pode encarecer o crédito para as empresas e famílias, o que pode prejudicar o crescimento económico.' +
    '<br>' +
    '<br>Os juros são a taxa de remuneração paga por um empréstimo ou financiamento. ' + 
    '<br>O spread bancário é a diferença entre a taxa de juros cobrada pelos bancos e a taxa de juros paga nas atividades de captação dos bancos. ' +
    '<br>' +
    '<br>Os juros são o que o banco paga para emprestar dinheiro, enquanto o spread bancário é o que o banco ganha com esse empréstimo.' +
    '<br>Por exemplo, se um banco cobra 10% de juros em um empréstimo e paga 5% de juros para captar recursos, o spread bancário é de 5%.'
 
    document.getElementById('info-spread').innerHTML = spread;
}

function infoTAN () {
    let tan = 'A TAN bancária é a taxa anual nominal, ou seja, a taxa de juros que é cobrada ou paga por um produto financeiro, como um empréstimo ou um investimento, ao longo de um ano.' +
    '<br>É calculada com base na taxa de juros mensal, multiplicada por 12. Por exemplo, se a taxa de juros mensal é de 1%, a TAN é de 12% ao ano.' +
    '<br>' +
    "<br>A TAN é importante para comparar diferentes produtos financeiros, pois permite avaliar o custo real de um empréstimo ou o rendimento de um investimento." +
    '<br>É um componente do spread bancário. O spread bancário é a diferença entre a taxa de juros cobrada pelos bancos e a taxa de juros paga nas atividades de captação dos bancos.' +
    '<br>A TAN é um indicador importante para avaliar os produtos financeiros, mas é importante considerar também o spread bancário para obter uma visão mais completa do custo real de um empréstimo ou do rendimento de um investimento.'
    
    document.getElementById('info-tan').innerHTML = tan;
}

